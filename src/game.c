void PRECIOS(int barrio){
  int c;
  for (c = 0; c < 8; ++c)
  {
  precioTEMP[c] = disp[barrio][c];
  }
  for (c = 0; c < 8; ++c)
  {
    char r;
    r = getR();
    if (r<64)
    {
    precioBARRIO[c] = precioTEMP[c] + precioVAR[c];
    int br;
    br = ((ran100()*3)/100)*precioVAR[c];
    precioBARRIO[c] = precioBARRIO[c] + br;
    }
    if (r>64)
    {
    precioBARRIO[c] = precioTEMP[c] - precioVAR[c];
    int br;
    br = ((ran100()*3)/100)*precioVAR[c];
    precioBARRIO[c] = precioBARRIO[c] - br;  
    }
  }
}

